# Operators 

# Arithmatic operators ( + - * / // % **)

print("Addition", 2+3)
print("Subtraction", 9-1)
print("Multiplication", 5*4)
print("Division", 7/3)

print("Floor division", 7//3)

print("Modulus", 7 % 3) 

print("Exponentiation", 6 ** 2)  # Power


# Relational (>, <, >=, <=, ==, i=) 

print(5 > 10)
print(10 > 5) 

print(5 > 5)
print(5 >= 5)
print(5 <=5 )

print(5 == 50)
print(5 == 5)

print(5 != 50)
print(5 != 5)

# Logical operators ( and, or, not)

print(True and True)
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

print(not True)
print(not False)
